def get_sensitive_file_remediation(filename: str) -> str:
    """Look up remediation for a sensitive file, checking exact then glob patterns."""
    if filename in SENSITIVE_FILE_REMEDIATION:
        return SENSITIVE_FILE_REMEDIATION[filename]
    import fnmatch as _fnmatch
    for pattern, remediation in SENSITIVE_FILE_REMEDIATION.items():
        if _fnmatch.fnmatch(filename, pattern):
            return remediation
    return _SENSITIVE_FILE_REMEDIATION_DEFAULT


