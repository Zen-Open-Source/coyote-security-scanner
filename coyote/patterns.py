"""Security detection patterns for Coyote scanner."""

from __future__ import annotations

import re
from dataclasses import dataclass, field
from enum import Enum
from typing import Optional


class Severity(Enum):
    HIGH = "HIGH"
    MEDIUM = "MEDIUM"
    LOW = "LOW"


@dataclass
class PatternMatch:
    rule_name: str
    severity: Severity
    file_path: str
    line_number: int
    line_content: str
    description: str
    matched_text: str = ""
    finding_id: str = ""  # Stable ID for diffing/suppression, generated by scanner
    remediation: str = ""  # Actionable fix guidance
    metadata: dict[str, object] = field(default_factory=dict)


@dataclass
class SecretPattern:
    name: str
    pattern: re.Pattern
    severity: Severity
    description: str
    remediation: str = ""
    near_context: Optional[re.Pattern] = None  # require nearby context


